
# 한줄 문자열 Chuỗi ký tự trên một dòng 
# \n là một escape sequence (이스케이프 시퀀스) ký tự thực hiện nhiệm vụ đặc biệt
# \n xuống dòng (enter)
print('"배가 고픕니다" 라고  말했습니다\n')

# 특수 문자 문자열 chuỗi ký tự chứa ký tự đặc biệt

