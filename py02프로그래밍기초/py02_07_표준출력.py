
# 하나만 출력합니다.

print("#하나만 출력합니다.")
print("Hello Python Programming...!")
print(10, 20, 30, 40, 50)
print() # Một cách để cách dòng
print("안녕하세요", "저는", "윤인성입니다!")
print() # Một cách để cách dòng

# 여러 개를 출력합니다.

print(10, 20, 30, 40, 50, end="\n") # end="\n" tức là kết thúc bằng việc xuống dòng
print("안녕하세요", "저는", "윤인성입니다!", end="\n")
print()

# 아무 것도 입력하지 않으면 단순하게 줄바꿈합니다. Nếu không thêm gì có nghĩa là xuống dòng

print(10,20,30,40,50, end="-")  #end="-" ở cuối đơn giản thêm một dấu gạch
print("안녕하세요", "저는", "윤인성입니다!", end=" ") #end="-" ở cuối đơn giản thêm một ô trống

